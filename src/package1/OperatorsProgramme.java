package package1;

public class OperatorsProgramme
{
	public static void main(String[] args)
	{
		// programme 1
		int a = 5;
		int b = 2;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		// programme 2 (++, --)
		int x = 12;
		x++;
		System.out.println(x);
		x--;
		System.out.println(x);
		
	}

}
