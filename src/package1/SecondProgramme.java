package package1;

public class SecondProgramme 
{
	public static void main(String[]args)
	{
		// programme1
	int a = 30;
	int b = 50;
	int sum = a+b;
	System.out.println(sum);
		
		// programme 2
		byte x = 10;
		byte y = 20;
		//byte z = x+y;                           casting
		int z = x+y;
		System.out.println(z);
		
		// programme 3
		short p = 25;
		short q = 30;
		//short r = p+q;                            casting
		int r = p+q;
		System.out.println(r);
		
		// difference between float and double 
		float e = 1.23456789101112131415f;
		System.out.println(e);
		double f = 1.23456789101112131415;
		System.out.println(f);
		
		// ASCII value programme
		char m = 'A';
		System.out.println(m);
		int n = m;                           // casting
		System.out.println(n);
		
		
	
		
		}

}
